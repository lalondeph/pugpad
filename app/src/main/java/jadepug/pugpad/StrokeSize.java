package jadepug.pugpad;

/**
 * StrokeSize class sets constants used to describe
 * and set the size of the paths stroke.
 */
public class StrokeSize {
    static final float SMALL = 10;
    static final float Med = 30;
    static final float LARGE = 50;

    public static float getSMALL() {
        return SMALL;
    }

    public static float getMed() {
        return Med;
    }

    public static float getLARGE() {
        return LARGE;
    }
}
